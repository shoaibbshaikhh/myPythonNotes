# str
first_name = "Shoaib"
last_name = "Shaikh"
full_name = first_name +" "+ last_name
print("Hello "+full_name)
print(type(first_name))

# int
age = 21
age += 1
print("Your age is: "+str(age))
print(type(age))

# float
height = 250.5
print("your height is: "+str(height)+"cm")
print(type(height))

# bool
human = True
print("Are you a human: "+str(human))
print(type(human))



# Output:
# Hello Shoaib Shaikh
# <class 'str'>
# Your age is: 22
# <class 'int'>
# your height is: 250.5cm
# <class 'float'>
# Are you a human: True
# <class 'bool'>