# 2D lists = a list of lists

drinks = ["coffe", "soda", "tea"]
dinner = ["pizza", "vada pav", "samosa"]
dessert = ["cake", "ice cream"]

food = [drinks, dinner, dessert]

print(food)
print(food[0])
print(food[1][1])



